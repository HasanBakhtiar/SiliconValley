//1stGroup => Pirimitive data type (Bəsit data tipləri)
//string
const stringInfoOne = 'Hello1';
const stringInfoTwo = "Hello2";
console.log(stringInfoOne);
console.log(stringInfoTwo);

//number
// const infoNumberOne = '5';
const infoNumberOne = 5;
const infoNumberTwo = 15.1;
console.log(infoNumberOne+infoNumberTwo);

// boolean
// true | false
const infoTrue = true;
console.log(infoTrue);


//2ndGroup => Composite data type (Birləşik data tipləri)
// Array
// const infoArrayN = new Array();
const infoArray = ["Hello", 5, true,10,11,"Salam"];
console.log(infoArray);

// Object
const infoObj = {
    name: "Orxan",
    surname: "Zeynalli",
    age: 24
}
console.log(infoObj.name);

// 3rdGroup => Special data type (Xüsusi data tipləri)
// undefined
const infoUndefined = undefined;
console.log(infoUndefined);
// null
const infoNull = null;
console.log(infoNull);





